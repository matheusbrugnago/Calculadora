import math as mat

def resultadoSoma(x,y):
    soma=x+y
    return soma

def resultadoSubtracao(x,y):
    sub=x-y
    return sub

def resultadoVezes(x,y):
    multiplicacao= x*y
    return multiplicacao

def resultadoDivisao(x,y):
    divisao=x/y
    return divisao

def resultadoPotencia(x,y):
    potencia= mat.pow(x,y)
    return potencia

def resultadoRaiz(x):
    raiz= mat.sqrt(x)
    return raiz